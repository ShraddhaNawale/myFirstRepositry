package Loops;

public class ForLoop {

	public static void main(String[] args) {
		// we want print 1=-100
		int x=1;
		for(x=1;x<=100;x++)
		{
			System.out.println(x);
		}
		
		// reverse of table 8
		for (int c=80;c>=8;c=c-8)
		{
		System.out.println(c);
		}
		//string name "prajakata "10 times
		String name="Prajakata";
		for(int p=1;p<=10;p++)
		
		{
			System.out.println(name);
		
	}


	//print 10 to 5 reverse order
	for(int h=10;h>=5;h--)
	{
		System.out.println(h);
	}
	

	}}
